# Understanding Arithmetic Operators

a = 20
b = 10
print(a + b)
print(a - b)
print(a * b)
print(a / b)

# Advanced Arithmetic Operators
print(a % 3) # Modulus
print(b ** 2) # Exponentiation ,i.e; b^2 = b * b 
print(b ** 3) # Exponentiation ,i.e; b^3 = b * b * b
print(a // b) # Floor Division
print(89 // 3) # Floor Division returns the integral quotient of the division discarding the remainder

# Assignment Operators
x = 5
print(x)

x = x + 3
print(x)
x += 3
print(x)

x = x - 3
print(x)
x -= 3
print(x)
# Similarly, we can use *=, /=, %=, **=, //= operators