# String Concatenation using '+' operator
I = "Hi there! Everyone."
N = "I'm Mobashir Ahmed."
G = "Nice to meet you."
C = "This is my contact number: "
n = "+91 9155511058."
S = "You can call me anytime."
# multiple concatenation
print(I + " " + N + " " + G)
# single concatenation and double parameter passing
print(C + n, S)

a = 20
c = a + 24
print(a+30)
print(c+a)