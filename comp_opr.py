# Comparison Operators

print("Enter two numbers to compare: ")
x = input()
y = input()
print("Say! Both no.s are equal: ",x == y)
print("Say! Both np.s aren't equal: ",x != y)
print("Say! First one is greater than the other: ",x > y)
print("Say! First one is less than the other: ",x < y)
print("Say! First one is greater than or equal to the other: ",x >= y)
print("Say! First one is less than or equal to the other: ",x <= y)