x = int(input("Enter value of x: "))
y = int(input("Enter value of y: "))

# Bitwise AND
print("\n")
print(bin(x))
print(bin(y))
print(bin(x & y), x & y)

# Bitwise OR
print("\n")
print(bin(x))
print(bin(y))
print(bin(x | y), x | y)

# Bitwise XOR
print("\n")
print(bin(x))
print(bin(y))
print(bin(x ^ y), x ^ y)